#include <stdio.h>
void main ()
{
int digit;
printf("Enter Any Digit Between 0-5\n");
scanf("%d",&digit);

	if (digit==1) //double equal sign must be there
		
		printf("spelling is one\n");

	else if (digit==2)
		
		printf("spelling is two\n");

	else if (digit==3)
		
		printf("spelling is three\n");

	else if (digit==4)
		
		printf("spelling is four\n");


}
